package com.logicpuzzles.slitherlink

// clues[r][c] = -1 (no clue) or 0..3
data class SlitherlinkPuzzle(
    val rows: Int,
    val cols: Int,
    val clues: Array<IntArray>
)

object SlitherlinkPuzzles {

    /** Each row is a string of single chars: '.' = no clue, digit = clue 0..3 */
    private fun build(text: String): SlitherlinkPuzzle {
        val rows = text.split("/")
        val cols = rows[0].length
        for (r in rows) require(r.length == cols) { "Row length mismatch in '$text'" }
        val clues = Array(rows.size) { r ->
            IntArray(cols) { c ->
                val ch = rows[r][c]
                if (ch == '.') -1 else ch.digitToInt()
            }
        }
        return SlitherlinkPuzzle(rows.size, cols, clues)
    }

    // 5x5 (5 rows of 5 chars)
    private val EASY = listOf(
        build(".2.2./..3../.2.2./...../....."),
        build("..0../.3.3./..2../.3.3./..0.."),
        build(".0.0./..1../.2.2./..1../.0.0."),
        build("...../..2../.232./..2../....."),
        build("3...3/...../...../...../3...3"),
        build("0...0/.2.2./..3../.2.2./0...0"),
        build("...../.2.2./..3../.2.2./....."),
        build("..3../.2.2./3...3/.2.2./..3.."),
        build(".1.1./..2../.1.1./..2../.1.1."),
        build("...../..1../.232./..1../.....")
    )

    // 6x6
    private val MEDIUM = listOf(
        build("..0.../.3..3./..2.2./..2.2./.3..3./..0..."),
        build("3....3/..2.2./.2..2./.2..2./..2.2./3....3"),
        build("..1.1./.2..2./..3.3./..3.3./.2..2./..1.1."),
        build("0....0/..2.2./.232.3/.2.232/..2.2./0....0"),
        build("3....3/.2..2./..3.3./..3.3./.2..2./3....3"),
        build("..2.2./.3..3./..2.2./..2.2./.3..3./..2.2."),
        build("..1.1./.2..2./..1.1./..1.1./.2..2./..1.1."),
        build(".2..2./3....3/..3.3./..3.3./3....3/.2..2."),
        build("0.3.3./..2.2./.3..3./.3..3./..2.2./.3.3.0"),
        build("..2.2./.1..1./..3.3./..3.3./.1..1./..2.2.")
    )

    // 7x7
    private val HARD = listOf(
        build("......./.3.3.3./.2.2.2./..3.3../.2.2.2./.3.3.3./......."),
        build(".0..0../.2..2../..3.3../..3.3../..3.3../.2..2../.0..0.."),
        build("3.....3/.2...2./..3.3../...3.../..3.3../.2...2./3.....3"),
        build("...3.../.2.2.2./..3.3../.23323./..3.3../.2.2.2./...3..."),
        build("0.....0/.3...3./..3.3../...3.../..3.3../.3...3./0.....0"),
        build("3.....3/.2...2./..3.3../..3.3../..3.3../.2...2./3.....3"),
        build("..1.1../.2...2./..3.3../...3.../..3.3../.2...2./..1.1.."),
        build("......./..3.3../..3.3../...3.../..3.3../..3.3../......."),
        build("3.0.0.3/.2...2./..3.3../..0.0../..3.3../.2...2./3.0.0.3"),
        build("...0.../.3...3./..3.3../..0.0../..3.3../.3...3./...0...")
    )

    // 8x8
    private val EXPERT = listOf(
        build("3......3/.2....2./..3..3../..2..2../..2..2../..3..3../.2....2./3......3"),
        build("0......0/.3....3./..3..3../..3..3../..3..3../..3..3../.3....3./0......0"),
        build("..2..2../.3....3./..3..3../..2..2../..2..2../..3..3../.3....3./..2..2.."),
        build("3.0..0.3/.3....3./..3..3../..0..0../..0..0../..3..3../.3....3./3.0..0.3"),
        build("..1..1../.3....3./..2..2../..3..3../..3..3../..2..2../.3....3./..1..1.."),
        build("3......3/......../...2.2../..23.32./..23.32./...2.2../......../3......3"),
        build("0......0/......../..3.3.../..3.3.../..3.3.../..3.3.../......../0......0"),
        build("..3..3../.2....2./..3..3../..3..3../..3..3../..3..3../.2....2./..3..3.."),
        build("..0..0../.3....3./..2..2../..3..3../..3..3../..2..2../.3....3./..0..0.."),
        build("3.0..0.3/.2....2./..3..3../..3..3../..3..3../..3..3../.2....2./3.0..0.3")
    )

    fun get(difficulty: Int, index: Int): SlitherlinkPuzzle {
        val pool = when (difficulty) {
            0 -> EASY; 1 -> MEDIUM; 2 -> HARD; else -> EXPERT
        }
        return pool[index.coerceIn(0, pool.size - 1)]
    }
}
