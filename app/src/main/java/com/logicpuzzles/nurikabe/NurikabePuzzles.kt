package com.logicpuzzles.nurikabe

// numbers[r][c] = 0 (no clue), >0 (island of that size starting from this cell)
data class NurikabePuzzle(
    val rows: Int,
    val cols: Int,
    val numbers: Array<IntArray>
)

object NurikabePuzzles {

    /** Each row is a string of single chars: '.' = empty, digit = island size. Rows separated by '/'. */
    private fun build(text: String): NurikabePuzzle {
        val rows = text.split("/")
        val cols = rows[0].length
        for (r in rows) require(r.length == cols) { "Row length mismatch in '$text'" }
        val grid = Array(rows.size) { r ->
            IntArray(cols) { c ->
                val ch = rows[r][c]
                if (ch == '.') 0 else ch.digitToInt()
            }
        }
        return NurikabePuzzle(rows.size, cols, grid)
    }

    // 5x5
    private val EASY = listOf(
        build("2..../...../..3../...../....1"),
        build("....1/...../..2../...../3...."),
        build("3..../...../...../...../....3"),
        build("..1../...../.2.3./...../..1.."),
        build("1.1.1/...../...../...../1.1.1"),
        build("2..../..1../...../..1../....2"),
        build("....2/...../..3../...../2...."),
        build("3.1.3/...../...../...../3.1.3"),
        build("1...1/...../..3../...../1...1"),
        build("2...2/...../..1../...../2...2")
    )

    // 6x6
    private val MEDIUM = listOf(
        build("2....2/....../..3.../...3../....../2....2"),
        build("3....1/....../..2.../...2../....../1....3"),
        build("....1./..3.../....../..2.../....../.1...."),
        build("1....2/....../..2.../...3../....../2....1"),
        build("3....3/....../..2.../...2../....../3....3"),
        build("..2.../....../1.3.1./....../..2.../......"),
        build("....3./....../..2.../....../.3..../......"),
        build("3....3/....../..1.../....../.3..3./......"),
        build("1.2.1./....../..3.../....../1.2.1./......"),
        build("2.1.2./....../.3.3../....../2.1.2./......")
    )

    // 7x7  (blank rows must be 7 chars: 7 dots)
    private val HARD = listOf(
        build("3....../......./..2.2../......./..2.2../......./......3"),
        build("4....../......./..3.3../......./..3.3../......./......4"),
        build("..1.1.1/......./.2.2.2./......./..1.1.1/......./......."),
        build("1.....3/......./..2.2../......./..2.2../......./3.....1"),
        build("3.....3/......./...4.../......./...4.../......./3.....3"),
        build("4.....4/......./..2.2../......./..2.2../......./4.....4"),
        build("..2..2./......./.3...3./......./.3...3./......./.2..2.."),
        build("1.....1/......./..3.3../......./..3.3../......./1.....1"),
        build("3.1.3.1/......./.1.5.1./......./3.1.3.1/......./......."),
        build("2.1.2.1/......./.4.4.4./......./2.1.2.1/......./.......")
    )

    // 8x8
    private val EXPERT = listOf(
        build("3......2/......../...4..../......../.....3../......../...4..../........"),
        build("5......1/......../.....3../......../......../......../1......5/........"),
        build("3.....1./......../...3.3../......../...3.3../......../3.....1./........"),
        build("2..3..2./......../...3..../......../4.....4./......../...3..../........"),
        build("..3...3./......../.4..1..4/......../...5.5../......../.4..1..4/........"),
        build("1.....3./......../..2.2.../......../3.....1./......../......../........"),
        build("4....4../......../...3..../...3..../......../4....4../......../........"),
        build("..3.3.../......../3.....3./......../.4...4../......../3.....3./........"),
        build("1...3..1/......../...4.4../......../..2.5.2./......../...4.4../........"),
        build("..4.4.../......../4.....4./......../..3.3.../......../4.....4./........")
    )

    fun get(difficulty: Int, index: Int): NurikabePuzzle {
        val pool = when (difficulty) {
            0 -> EASY; 1 -> MEDIUM; 2 -> HARD; else -> EXPERT
        }
        return pool[index.coerceIn(0, pool.size - 1)]
    }
}
